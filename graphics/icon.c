int8_t reverse_font(int8_t f){
    int8_t *fon;
    int8_t letter [8][8];
    int i,j,k,l,sum;
    for(int i = 0; i<8; i++) {
        for (j = 0; j < 8; j++) {
            l = (f >> j) & 1;
            letter[i][j] = l;
        }

        while (k < 8) {
            sum += letter[k][i] * 2;
            k++;
        }
    }
       // f[i] = sum;
    return sum;
}

